package day5_unary_assignment_relational_operators;

public class AssingOperators {

	public static void main(String[] args) {
		
		int i = 100;
		System.out.println(i);
		//as "i" is a variable it will be take its new value result for next operation
		i+=2;
		System.out.println(i);		
		i-=2;
				System.out.println(i);	
		i/=2;
				System.out.println(i);	
		i*=2;
				System.out.println(i);	
		i%=2;
				System.out.println(i);	
		
	}

}

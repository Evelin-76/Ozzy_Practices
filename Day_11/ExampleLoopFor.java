package Day_11;

public class ExampleLoopFor {

	public static void main(String[] args) {
		
		int x=0;
		int y=0;
		for( y=0; x<5 && y<10; x++, y++) {
			
			System.out.print(y + " ");
			
			System.out.print(x + " ");
			
		}
		System.out.println();
		
		System.out.println(x+y);

	}
	
	

}
